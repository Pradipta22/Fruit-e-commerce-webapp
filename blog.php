				<p>Accordingly, fruits account for a substantial fraction of the world's agricultural output, and some
					(such as the apple and the pomegranate) have acquired extensive cultural and symbolic meanings.</p>

				<p>In common language usage, "fruit" normally means the fleshy seed-associated structures of a plant
					that are sweet or sour, and edible in the raw state, such as apples, bananas, grapes, lemons,
					oranges, and strawberries.</p>

				<p>On the other hand, in botanical usage, "fruit" includes many structures that are not commonly called
					"fruits", such as bean pods, corn kernels, tomatoes, and wheat grains.</p>

				<p>The section of a fungus that produces spores is also called a fruiting body.</p>
