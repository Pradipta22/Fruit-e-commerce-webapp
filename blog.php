			<div class="text">
				<p>In botany, a fruit is the seed-bearing structure in flowering plants (also known as angiosperms)
					formed from the ovary after flowering.</p>

				<p>Fruits are the means by which angiosperms disseminate seeds.Edible fruits, in particular, have
					propagated with the movements of humans and animals in a symbiotic relationship as a means for seed
					dispersal and nutrition; in fact, humans and many animals have become dependent on fruits as a
