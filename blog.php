			<h2>Development</h2>
			<div class="imgbox">
				<img src="./blogimg/Nectarine Fruit.jpg">
			</div>
			<div  class="text">
				<p>A fruit results from maturation of one or more flowers, and the gynoecium of the flower(s) forms all
					or part of the fruit.</p>
				<p>Inside the ovary/ovaries are one or more ovules where the megagametophyte contains the egg cell.After
					double fertilization, these ovules will become seeds.The ovules are fertilized in a process that
					starts with pollination, which involves the movement of pollen from the stamens to the stigma of
					flowers. After pollination, a tube grows from the pollen through the stigma into the ovary to the
					ovule and two sperm are transferred from the pollen to the megagametophyte.Within the
					megagametophyte one of the two sperm unites with the egg, forming a zygote, and the second sperm
					enters the central cell forming the endosperm mother cell, which completes the double fertilization
					process.Later the zygote will give rise to the embryo of the seed, and the endosperm mother cell
					will give rise to endosperm, a nutritive tissue used by the embryo.</p>

				<p>There are three general modes of fruit development:</p>
			</div>
			<div class="tags">
				<ul>
					<li>Apocarpous fruits develop from a single flower having one or more separate carpels, and they
						are the simplest fruits.</li>
					<li>Syncarpous fruits develop from a single gynoecium having two or more carpels fused together.
					</li>
					<li>Multiple fruits form from many different flowers.</li>
				</ul>
			</div>

