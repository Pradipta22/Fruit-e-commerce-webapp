			<h2>Seedless fruits</h2>
			<div class="imgbox">
				<img src="./blogimg/Dish.jpg">
			</div>
			<div  class="text">
				<p>Seedlessness is an important feature of some fruits of commerce. Commercial cultivars of bananas and
					pineapples are examples of seedless fruits. Some cultivars of citrus fruits (especially grapefruit,
					mandarin oranges, navel oranges), satsumas, table grapes, and watermelons are valued for their
					seedlessness. In some species, seedlessness is the result of parthenocarpy, where fruits set without
					fertilization. Parthenocarpic fruit set may or may not require pollination, but most seedless citrus
					fruits require a stimulus from pollination to produce fruit.</p>
				<p>Seedless bananas and grapes are triploids, and seedlessness results from the abortion of the
					embryonic plant that is produced by fertilization, a phenomenon known as stenospermocarpy, which
					requires normal pollination and fertilization.</p>
