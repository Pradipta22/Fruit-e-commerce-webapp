			<h2>Botanic fruit and culinary fruit</h2>

			<div class="imgbox">
				<img src="./blogimg/Culinary fruit.jpg">
			</div>
			<div  class="text">
				<p>Many common terms for seeds and fruit do not correspond to the botanical classifications. In culinary
					terminology, a fruit is usually any sweet-tasting plant part, especially a botanical fruit; a nut is
					any hard, oily, and shelled plant product; and a vegetable is any savory or less sweet plant
					product.</p>

				<p>However, in botany, a fruit is the ripened ovary or carpel that contains seeds, a nut is a type of
					fruit and not a seed, and a seed is a ripened ovule.</p>



				<p>Botanically, a cereal grain, such as corn, rice, or wheat, is also a kind of fruit, termed a
					caryopsis. However, the fruit wall is very thin and is fused to the seed coat, so almost all of the
					edible grain is actually a seed.</p>
			</div>
